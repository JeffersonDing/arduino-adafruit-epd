static void draw(void) {
  Adafruit_EPD *epd = mgos_aepd_get_global();
  mgos_aepd_clear_buffer(epd);
  mgos_aepd_set_text_wrap(epd, true);
  mgos_aepd_set_text_color(epd, 0);

  const int img_dim = 50;
  mgos_aepd_draw_bitmap(epd, 0, 0, (uint8_t *) mgos_logo, img_dim, img_dim, 0);

  mgos_aepd_set_cursor(epd, 0, img_dim + 2);

  mgos_aepd_set_text_size(epd, 2);
  mgos_aepd_print(epd, "Hello World!");

  int curr_x = mgos_aepd_get_cursor_x(epd);
  mgos_aepd_println(epd, "");
  int curr_y = mgos_aepd_get_cursor_y(epd);

  const int gfx_prim_dim = 20;
  const int gfx_prim_offset = 5;

  mgos_aepd_draw_line(epd, 0, curr_y, curr_x, curr_y, 0);
  curr_x = 0;
  curr_y = curr_y + gfx_prim_offset;

  mgos_aepd_draw_rect(epd, 0, curr_y, gfx_prim_dim, gfx_prim_dim, 0);
  curr_x = gfx_prim_dim + gfx_prim_offset;

  mgos_aepd_fill_rect(epd, curr_x, curr_y, gfx_prim_dim, gfx_prim_dim, 0);
  curr_x = curr_x + gfx_prim_dim + gfx_prim_offset;

  mgos_aepd_draw_circle(epd, curr_x + gfx_prim_dim / 2, curr_y + gfx_prim_dim / 2, gfx_prim_dim / 2, 0);
  curr_x = curr_x + gfx_prim_dim + gfx_prim_offset;

  mgos_aepd_fill_circle(epd, curr_x + gfx_prim_dim / 2, curr_y + gfx_prim_dim / 2, gfx_prim_dim / 2, 0);
  curr_x = curr_x + gfx_prim_dim + gfx_prim_offset;

  mgos_aepd_draw_triangle(epd, curr_x, curr_y + gfx_prim_dim, curr_x + gfx_prim_dim / 2, curr_y, curr_x + gfx_prim_dim, curr_y + gfx_prim_dim, 0);
  curr_x = curr_x + gfx_prim_dim + gfx_prim_offset;

  mgos_aepd_fill_triangle(epd, curr_x, curr_y + gfx_prim_dim, curr_x + gfx_prim_dim / 2, curr_y, curr_x + gfx_prim_dim, curr_y + gfx_prim_dim, 0);
  curr_x = curr_x + gfx_prim_dim + gfx_prim_offset;

  mgos_aepd_draw_round_rect(epd, curr_x, curr_y, gfx_prim_dim, gfx_prim_dim, 5, 0);
  curr_x = curr_x + gfx_prim_dim + gfx_prim_offset;

  mgos_aepd_fill_round_rect(epd, curr_x, curr_y, gfx_prim_dim, gfx_prim_dim, 5, 0);
  curr_x = curr_x + gfx_prim_dim + gfx_prim_offset;

  curr_x = 0;
  curr_y = curr_y + gfx_prim_dim + gfx_prim_offset;

  char *header = "Made with <3 in Berlin";
  mgos_aepd_set_text_size(epd, 1);

  int16_t ftb_x;
  int16_t ftb_y;
  int16_t ftb_w;
  int16_t ftb_h;

  mgos_aepd_get_text_bounds(epd, header, 0, 0, &ftb_x, &ftb_y, &ftb_w, &ftb_h);
  mgos_aepd_set_cursor(epd, mgos_aepd_width(epd) - ftb_w, mgos_aepd_height(epd) - ftb_h);
  mgos_aepd_print(epd, header);

  mgos_aepd_display(epd);
}

enum mgos_app_init_result mgos_app_init(void) {
  draw();
  return MGOS_APP_INIT_SUCCESS;
}
